empty_list = []
print("1. Empty List:", empty_list)

my_list = [10, 20, 30, 40, 50]
print("2. List with elements:", my_list)

repeated_list = my_list * 3
print("3. List after using * operator:", repeated_list)

reversed_list = my_list[::-1]
print("4. Reversed List (using slicing):", reversed_list)

my_list.reverse()
print("   Reversed List (using reverse() function):", my_list)